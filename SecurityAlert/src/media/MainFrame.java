    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        String text = delM.getText();
        if(box1.getSelectedItem()!=null)
        {
            String m = box1.getSelectedItem().toString();
            if(!text.equals("")){
                String line = "";
            int k=0;
            try {
                BufferedReader bfr = new BufferedReader(new FileReader(folder+"/"+m+mots));
                BufferedWriter bf = new BufferedWriter(new FileWriter(folder+"/"+m+"/temp.txt",true));
                while((line = bfr.readLine())!=null){
                    if(!line.equals(text)){
                        bf.write(line);
                        bf.newLine();
                        }
                    else
                        k++;
                }
                bfr.close();
                bf.close();
                FileChannel src = new FileInputStream(folder+"/"+m+"/temp.txt").getChannel();
                FileChannel dest = new FileOutputStream(folder+"/"+m+mots).getChannel();
                dest.transferFrom(src, 0, src.size());
                src.close();
                dest.close();
                File f= new File(folder+"/"+m+"/temp.txt");
                f.delete();
                delM.setText("");
                if(k==0)
                    JOptionPane.showMessageDialog(null, "Le mot "+text+" n'existe pas!");
                else
                    JOptionPane.showMessageDialog(null, "le mot "+text+" est supprimé");
            } catch (Exception ex) {
                Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            }
            else
                JOptionPane.showMessageDialog(null, "Tapez un mot à supprimer !!!");
        }
        else
            JOptionPane.showMessageDialog(null, "Pas de programme !!!");
    }//GEN-LAST:event_jButton17ActionPerformed

